public class Calculo {
    public static double suma(float numero1, float numero2) {
        return numero1 + numero2;
    }

    public static double resta(float numero1, float numero2) {
        return numero1 - numero2;
    }

    public static double multiplicacion(float numero1, float numero2) {
        return numero1 * numero2;
    }

    public static double division(float numero1, float numero2) {
        return numero1 / numero2;
    }
}
